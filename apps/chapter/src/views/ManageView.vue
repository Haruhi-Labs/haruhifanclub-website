function filterIndexed(items, query, getValues) {
  const needle = query.trim().toLocaleLowerCase('zh-CN')
  return items
    .map((item, index) => ({ item, index }))
    .filter(({ item }) => {
      if (!needle) return true
      return getValues(item).some((value) =>
        String(value || '')
          .toLocaleLowerCase('zh-CN')
          .includes(needle)
      )
    })
}
