function eventStepErrors(section) {
  const issues = []
  if (section === 'details') {
    if (!contentForm.title?.trim()) issues.push('请填写活动标题')
    if (!contentForm.slug?.trim()) issues.push('请填写活动链接标识')
    if (!contentForm.startsAt) issues.push('请选择活动开始时间')
    if (contentForm.status === 'published' && !contentForm.endsAt) {
      issues.push('发布活动前请填写结束时间')
    }
    if (contentForm.endsAt && contentForm.startsAt && contentForm.endsAt <= contentForm.startsAt) {
      issues.push('结束时间必须晚于开始时间')
    }
  } else if (section === 'registration') {
    if (
      ['external', 'both'].includes(contentForm.registrationMode) &&
      !contentForm.registrationUrl?.trim()
    ) {
      issues.push('外部报名方式需要填写报名链接')
    }
    if (contentForm.capacity !== null && contentForm.capacity !== '' && contentForm.capacity < 1) {
      issues.push('人数上限必须大于 0')
    }
    if (
      contentForm.registrationOpensAt &&
      contentForm.registrationClosesAt &&
      contentForm.registrationClosesAt < contentForm.registrationOpensAt
    ) {
      issues.push('报名截止时间不能早于开放时间')
    }
    for (const [index, question] of eventOperations.questions.entries()) {
      if (!question.label?.trim()) issues.push(`报名问题 ${index + 1} 尚未填写题目`)
      if (question.questionType !== 'short_text' && !question.optionsText?.trim()) {
        issues.push(`报名问题 ${index + 1} 尚未填写选项`)
      }
    }
  } else if (section === 'people') {
    eventOperations.people.forEach((person, index) => {
      if (!person.name?.trim()) issues.push(`活动人员 ${index + 1} 尚未填写姓名`)
    })
    eventOperations.partners.forEach((partner, index) => {
      if (!partner.name?.trim()) issues.push(`合作方 ${index + 1} 尚未填写名称`)
    })
  } else if (section === 'cohosts') {
    eventOperations.cohosts.forEach((cohost, index) => {
      if (!cohost.branchId) issues.push(`联合主办邀请 ${index + 1} 尚未选择支部`)
    })
  }
  return issues
}
