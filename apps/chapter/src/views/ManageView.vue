function suggestEventSlug(title) {
  const ascii = String(title || '')
    .normalize('NFKD')
    .toLocaleLowerCase('en-US')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
    .slice(0, 56)
  if (ascii) return ascii
  const day = String(contentForm.startsAt || new Date().toISOString())
    .slice(0, 10)
    .replaceAll('-', '')
  let hash = 0
  for (const character of String(title || '活动')) {
    hash = (hash * 31 + character.codePointAt(0)) >>> 0
  }
  return `event-${day}-${hash.toString(36)}`
}
