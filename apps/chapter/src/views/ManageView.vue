function saveContent() {
  if (panel.value === 'albums') {
    const issue = !contentForm.eventId
      ? '请选择照片所属的活动'
      : !contentForm.happenedAt
        ? '请填写照片拍摄时间'
        : !contentForm.title.trim()
          ? '请填写照片标题'
          : contentForm.status === 'published' && !contentForm.imagePath
            ? '公开相册照片前请先上传图片'
            : ''
    if (issue) {
      error.value = issue
      toast.danger(issue, { title: '相册照片尚未完成', duration: 6000 })
      return
    }
  }
  return runSave(
    async () => {
      const body = { ...contentForm }
      if (body.id) {
        await api.patch(`/admin/branches/${branchId.value}/${contentKind.value}/${body.id}`, body)
      } else {
        await api.post(`/admin/branches/${branchId.value}/${contentKind.value}`, body)
      }
      if (panel.value === 'events' && body.id) {
        const result = await api.get(`/admin/branches/${branchId.value}/events`)
        contentItems.value = result.items || []
        branchEvents.value = result.items || []
      } else {
        await loadPanel()
      }
    },
    contentForm.id ? '内容已更新' : '内容已创建'
  )
}
async function deleteContent() {
  if (!contentForm.id) return
  const deletingAlbum = panel.value === 'albums'
  const confirmed = await requestConfirmation({
    title: deletingAlbum ? '删除相册照片' : '删除活动',
    copy: deletingAlbum
      ? '确定删除这张相册照片吗？删除后将不再显示在活动详情中。'
      : '确定删除这个活动吗？删除后不会继续公开显示。',
    label: deletingAlbum ? '确认删除照片' : '确认删除活动',
  })
  if (!confirmed) return
  const deleted = await runSave(
    async () => {
      await api.del(`/admin/branches/${branchId.value}/${contentKind.value}/${contentForm.id}`)
      await loadPanel()
      return true
    },
    deletingAlbum ? '相册照片已删除' : '活动已删除'
  )
  if (deleted && panel.value === 'events') {
    await router.push(`/branches/${slug.value}/manage/events`)
  }
}
