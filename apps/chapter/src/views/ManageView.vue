async function loadSnapshot() {
  const snapshot = await api.get(`/admin/branches/${branchId.value}`)
  Object.assign(branch, snapshot.branch)
  Object.assign(brand, snapshot.brand || {})
  sections.value = normalizeSections(snapshot.sections || [])
  merchandise.value = hydrateMerchandise(snapshot.merchandise || [])
  members.value = (snapshot.members || []).map((item) => ({ ...item }))
  qqGroups.value = (snapshot.qqGroups || []).map((item) => ({ ...item }))
  const methods = snapshot.contacts?.methods || []
  contacts.value = (snapshot.contacts?.people || []).map((person) => ({
    ...person,
    consentGranted: !!person.consentConfirmedAt,
    methods: methods
      .filter((method) => method.personId === person.id)
      .map((method) => ({ ...method })),
  }))
  const org = snapshot.organization || blankOrganization()
  Object.assign(organization, blankOrganization(), org, {
    units: (org.units || []).map((item) => ({ ...item })),
    assignments: (org.assignments || []).map((item) => ({ ...item })),
  })
}

let loadedPanelKey = ''
async function syncRouteState() {
  const previousSubpanel = subpanel.value
  let freshEditor = false

  if (panel.value !== 'events') {
    const options = currentSubpanels.value
    const requested = String(route.params.subpanel || '')
    const next = options.some((item) => item[0] === requested) ? requested : options[0]?.[0] || ''
    if (options.length && requested !== next) {
      await router.replace(manageSubpanelPath(next))
      return
    }
    subpanel.value = next
    contentEditorOpen.value = false
    freshEditor = previousSubpanel !== next
  } else if (route.name === 'branch-manage-event-new') {
    const requested = String(route.params.section || 'details')
    const next = eventCreationSections.some((item) => item[0] === requested) ? requested : 'details'
    if (requested !== next) {
      await router.replace(`/branches/${slug.value}/manage/events/new/${next}`)
      return
    }
    const wasCreating = previousSubpanel === 'create' && contentEditorOpen.value
    subpanel.value = 'create'
    eventEditorSection.value = next
    contentEditorOpen.value = true
    if (!wasCreating) {
      replaceContentForm(blankContent('events'))
      Object.assign(eventOperations, blankEventOperations())
      registrations.value = []
      autoGeneratedSlug.value = ''
      freshEditor = true
    }
  } else if (route.name === 'branch-manage-event-invitations') {
    subpanel.value = 'invitations'
    contentEditorOpen.value = false
    eventEditorSection.value = 'details'
    freshEditor = previousSubpanel !== 'invitations'
  } else if (route.name === 'branch-manage-event-edit') {
    const eventId = Number(route.params.eventId)
    const item = contentItems.value.find((entry) => Number(entry.id) === eventId)
    if (!item) {
      error.value = '找不到这项活动，可能已被删除或不属于当前支部'
      await router.replace(`/branches/${slug.value}/manage/events`)
      return
    }
    const allowedSections = eventEditingSections.value
    const requested = String(route.params.section || 'details')
    const next = allowedSections.some((entry) => entry[0] === requested) ? requested : 'details'
    if (requested !== next) {
      await router.replace(`/branches/${slug.value}/manage/events/${eventId}/${next}`)
      return
    }
    const changedEvent = Number(contentForm.id) !== eventId || previousSubpanel !== 'existing'
    subpanel.value = 'existing'
    eventEditorSection.value = next
    contentEditorOpen.value = true
    if (changedEvent) {
      replaceContentForm({
        ...blankContent('events'),
        ...item,
        startsAt: toDateTimeLocal(item.startsAt),
        endsAt: toDateTimeLocal(item.endsAt),
        registrationOpensAt: toDateTimeLocal(item.registrationOpensAt),
        registrationClosesAt: toDateTimeLocal(item.registrationClosesAt),
      })
      await loadEventOperations(eventId)
      freshEditor = true
    }
  } else {
    subpanel.value = 'existing'
    contentEditorOpen.value = false
    eventEditorSection.value = 'details'
    freshEditor = previousSubpanel !== 'existing'
  }

  if (freshEditor || !savedFingerprint.value) {
    await nextTick()
    markSaved()
  }
}

